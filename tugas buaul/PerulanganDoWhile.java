public class PerulanganDoWhile {
    
    public static void main(String[] args) {
        //While
        int j=4;
        while (j <= 3) {
            System.out.println(j);
            j++;
        }




        int i = 4;
        do {
            System.out.println(i);
        } while (i <= 3);
    }
}
